export class Collegues {

  id: string;
  userName: string;
  pictureURL: string;
  lastName: string;
  firstName: string;

  constructor(id, userName, pictureUrl, lastName, firstName) {
    this.id = id;
    this.userName = userName;
    this.pictureURL = pictureUrl;
    this.lastName = lastName;
    this.firstName = firstName;
  }


}
